import UIKit

extension UIButton {
    
    func setSelectedColor() {
        self.backgroundColor = #colorLiteral(red: 0.314944625, green: 0.6045764594, blue: 0.1274076236, alpha: 1)
    }
    func setDeselectedColor() {
        self.backgroundColor = #colorLiteral(red: 0.518664433, green: 1, blue: 0.2052372644, alpha: 1)
        
    }
}
